#include <stdio.h>
//특수문자 (excape character)
// \(Backslash) 다음에 excape character를 사용한다.
// 참고) /(shlash)
void main(){
	// \n <= 줄바꿈
	// 제목: 7+8*2=23
	printf("   <IoT 빅데이터반 주소록>\n");

	// 전체 공간 8+8+14=30
	printf(" 이름\t  나이\t   전화번호\n");
				// 14개의 공간 사용
	printf("손병찬\t   29\t 010-0000-0000\n");
	printf("김혜경\t   20\t 010-2222-1111\n");

	printf("------------------------------\n");
	printf("출력완료!\n\a");
	printf("/(slash) 출력\n");
	// 아래 코드는 \( <= 이런 문법이 없기 때문에
	// compile 시 warning 메세지가 출력됐고
	// 실행시 해당 문자열 이후의 문자열을 무시되었다.
	// 빌드 또는 실행시 발생되는 상황은
	// 컴파일러에 따라 그 때 그 때 다르다.
	//printf("\(backslash)");
	printf("\\(backslash) 출력\n");
	
	printf("여기 숫자 있어요. 1234\n");
	printf("뒤에 있는 숫자하나를 바꿀겁니다. 1234\b5 \n");
	printf("300 을 400으로 바꿔볼게요. \r4\n");
	printf("300 을 433으로 바꿔볼게요. \r433\n");

}
